# Набор 1: Обычный случай с циклическим сдвигом
A1 = "abcde"
B1 = "deabc"
# Ожидаемый результат: 4

# Набор 2: Строки одинаковые
A2 = "abcdef"
B2 = "abcdef"
# Ожидаемый результат: 0

# Набор 3: Строки разные, циклический сдвиг невозможен
A3 = "abc"
B3 = "def"
# Ожидаемый результат: -1

# Набор 4: Пустая строка
A4 = ""
B4 = ""
# Ожидаемый результат: 0

# Набор 5: Строка A является подстрокой B, но сдвиг не равен 0
A5 = "abc"
B5 = "cab"
# Ожидаемый результат: 2

# Набор 6: Строка A длиннее строки B (невозможно циклический сдвиг)
A6 = "abcd"
B6 = "abc"
# Ожидаемый результат: -1

# Набор 7: Сдвиг на несколько позиций
A7 = "yzabc"
B7 = "abcyz"
# Ожидаемый результат: 3

# Набор 8: Тест с большими строками
A8 = "a" * 1000 + "b"
B8 = "b" + "a" * 1000
# Ожидаемый результат: 999

# Набор 9: Когда строка A не является циклическим сдвигом строки B
A9 = "abcd"
B9 = "dcba"
# Ожидаемый результат: -1

# Набор 10: Большие строки с циклическим сдвигом
A10 = "a" * 1000000 + "b"
B10 = "b" + "a" * 1000000
# Ожидаемый результат: 1000000
